
public class Node {

	int id;
	double sum;
	public Node(int id, double sum) {
		super();
		this.id = id;
		this.sum = sum;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
}
